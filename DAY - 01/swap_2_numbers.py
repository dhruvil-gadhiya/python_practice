# Swap the values of two variables...

a = int(input("Enter the value for 'a' : "))
b = int(input("Enter the value for 'b' : "))

print("\n")

print("--- Befor swap value of a & b ---")
print("value of a : ",a)
print("value of b : ",b)

print("\n")
a,b = b,a

print("--- After swap value of a & b is ---")

print("value of a : ",a)
print("value of b : ",b)
